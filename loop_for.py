primes = [2, 3, 5, 7]
for prime in primes:
    print(prime)

# Prints out the numbers 0,1,2,3,4 (all numbers up to 5)
for x in range(5):
    print(x)

# Prints out 3,4,5 (all numbers in range 3 to 6)
for x in range(3, 6):
    print(x)

# Prints out 3,5,7 (all uneven numbers between 3 and 8)
for x in range(3, 8, 2):
    print(x)
